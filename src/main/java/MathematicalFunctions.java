public class MathematicalFunctions {
    public static void main(String[] args) {

        double x = 2.0;
        double y = 3.0;
        double z = -7.0;

        double a = Math.pow(x,y); // exponentiation
        System.out.println(a);

        double b = Math.sqrt(y); // square root
        System.out.println(b);

        double c = Math.PI;
        System.out.println(c);

        double d = Math.abs(z); // absolute value
        System.out.println(d);

        // It is possible to use functions inside larger expressions


    }
}
